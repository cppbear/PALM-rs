#[test]
fn test_build_hex_table_larger_shift() {
    let result = build_hex_table(16);
    for i in 0..256 {
        if i < 32 || (i > 32 && i < 48) || (i > 48 && i < 64) || (i > 64 && i < 80) || (i > 80 && i < 96) || (i > 96 && i < 112) || (i > 112) {
            assert_eq!(result[i], -1);
        } else {
            assert_eq!(result[i], (i as i16) << 16);
        }
    }
}

